export function getRadioStationName(
	index: number | RadioStation,
): string | undefined {
	// Add radio stations not in regular data
	if (index === 100) {
		return 'Blonded FM';
	}

	if (index === 101) {
		return 'Los Santos Underground';
	}

	// Above 85 is considered "off"
	if (index > 85) {
		return 'Off';
	}

	return names[index];
}

